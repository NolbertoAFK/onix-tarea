drop policy if exists "roles visibles para autenticados" on public.roles;
create policy "roles visibles para autenticados"
on public.roles for select
to authenticated
using (true);

drop policy if exists "perfiles visibles por dueno o admin" on public.perfiles;
create policy "perfiles visibles por dueno o admin"
on public.perfiles for select
to authenticated
using (id = auth.uid() or private.is_admin());

drop policy if exists "usuarios actualizan su nombre" on public.perfiles;
create policy "usuarios actualizan su nombre"
on public.perfiles for update
to authenticated
using (id = auth.uid())
with check (id = auth.uid());

drop policy if exists "vacaciones visibles por dueno o admin" on public.vacaciones;
create policy "vacaciones visibles por dueno o admin"
on public.vacaciones for select
to authenticated
using (usuario_id = auth.uid() or private.is_admin());

drop policy if exists "usuarios crean sus vacaciones" on public.vacaciones;
create policy "usuarios crean sus vacaciones"
on public.vacaciones for insert
to authenticated
with check (
  usuario_id = auth.uid()
  and estado = 'pendiente'
  and autorizado_por is null
  and fecha_respuesta is null
);

drop policy if exists "admins resuelven vacaciones" on public.vacaciones;
create policy "admins resuelven vacaciones"
on public.vacaciones for update
to authenticated
using (private.is_admin())
with check (private.is_admin());

